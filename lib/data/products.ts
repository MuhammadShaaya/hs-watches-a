function img(seed: string, alt: string, isMain = false, order = 0): ProductImage {
  return {
    id: `${seed}-${order}`,
    url: `/products/${seed.split("-").slice(0, -1).join("-")}/${order + 1}.jpg`,
    alt,
    isMain,
    order,
  };
}

