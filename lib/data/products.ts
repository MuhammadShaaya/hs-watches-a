function specs(caseM: string, size: string, movement: string, water: string, glass: string, power: string) {
  return [
    { label: "Case Material", value: caseM },
    { label: "Case Diameter", value: size },
    { label: "Movement", value: movement },
    { label: "Water Resistance", value: water },
    { label: "Crystal", value: glass },
    { label: "Power Reserve", value: power },
  ];
}

function genReviews(productId: string, names: string[], baseRating: number) {
  const comments = [
    "Exceeded my expectations. The finishing is immaculate and it wears beautifully on the wrist.",
    "Worth every dollar. The weight and craftsmanship feel genuinely high-end.",
    "Arrived in pristine condition. Packaging alone felt like a luxury experience.",
    "The dial detail is stunning in person — photos really don't do it justice.",
    "A timeless piece. I get compliments every time I wear it.",
  ];
  return names.map((n, i) => ({
    id: `${productId}-rev-${i}`,
    productId,
    customerName: n,
    rating: Math.min(5, Math.max(3, baseRating + (i % 2 === 0 ? 0 : -1))),
    title: i % 2 === 0 ? "Exceptional craftsmanship" : "Stunning piece",
    comment: comments[i % comments.length],
    date: new Date(Date.now() - i * 12 * 86400000).toISOString(),
    verifiedPurchase: true,
    helpfulCount: 4 + i * 3,
  }));
}

