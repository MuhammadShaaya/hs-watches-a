export const PRODUCTS: Product[] = RAW.map((p, idx) => {
  const images: ProductImage[] = [
    img(`${p.slug}-1`, `${p.name} — front view`, true, 0),
    img(`${p.slug}-2`, `${p.name} — side profile`, false, 1),
    img(`${p.slug}-3`, `${p.name} — on wrist`, false, 2),
    img(`${p.slug}-4`, `${p.name} — caseback detail`, false, 3),
  ];
  const names = REVIEW_NAMES[idx % REVIEW_NAMES.length];
  const baseRating = 4 + (idx % 2);
  const reviews = genReviews(p.id, names, baseRating);
  const avgRating =
    Math.round((reviews.reduce((s, r) => s + r.rating, 0) / reviews.length) * 10) / 10;

  return {
    ...p,
    images,
    reviews,
    rating: avgRating,
    reviewCount: reviews.length + 12 + idx * 3,
    createdAt: new Date(Date.now() - (idx + 1) * 9 * 86400000).toISOString(),
  };
});

export function getProductBySlug(slug: string) {
  return PRODUCTS.find((p) => p.slug === slug);
}

export function getProductById(id: string) {
  return PRODUCTS.find((p) => p.id === id);
}

export function getRelatedProducts(product: Product, count = 4) {
  return PRODUCTS.filter(
    (p) => p.id !== product.id && p.collection === product.collection
  ).slice(0, count);
}
